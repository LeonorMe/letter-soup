/**
 * getT
 * ─────────────────────────────────────────────────────────────────────────────
 * Returns the translation object for the given language code.
 * Falls back to English if the code is not recognised.
 *
 * @param {string} langCode  e.g. 'en', 'pt', 'es', 'it', 'de', 'fr'
 * @returns {Object} flat translation object
 */
export function getT(langCode) {
  return translations[langCode] || translations.en;
}
